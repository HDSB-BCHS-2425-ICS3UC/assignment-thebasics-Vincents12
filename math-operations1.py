# Addition: Adds numbers together
addition = 5 + 5
print("Adds numbers together: 5 + 5 = ", addition)

# Subtraction: Subtracts the 2nd number from the 1st number
subtraction = 5 - 5
print("Subtracts the 2nd number from the 1st number: 5 - 5 = ", subtraction)

# Multiplication: Multiplies numbers together
multiplication = 5 * 5
print("Multiplies numbers together: 5 * 5 = ", multiplication)

# Division Float: Divides the 1st number by the 2nd number (result = float)
division_float = 5 / 4
print("Divides the 1st number by the 2nd number (result = float): 5 / 4 = ", division_float)

# Division Integer: Divides the 1st number by the 2nd number (result = integer)
division_integer = 5 // 4
print("Divides the 1st number by the 2nd number (result = integer): 5 // 4 = ", division_integer)

# Modulus: Returns the remainder of the division 
modulus_1 = 5 % 4
print("Returns the remainder of the division: 5 % 4 = ", modulus_1)

# Square Root: Calculates the square root of a number 
import math
square_root = math.sqrt(25)
print("Calculates the square root of a number: math.sqrt(25) = ", square_root)

# Exponent: Raises the 1st number to the power of the exponent
exponent = 2**5
print("Raises the 1st number to the power of the exponent: 2**5 = ", exponent)