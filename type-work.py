# Integer: A whole number
integer = 42
print("This is an integer:", integer)

# Float: A decimal
float = 3.14
print("This is a float:", float)

# String: A number of characters 
string = "I sometimes like computer science."
print("This is a string:", string)

# Boolean: True or False
boolean = True
print("This is a boolean:", boolean)

# Double: A float but more accurate
double = -3.94872405
print("This is a double:", double)

# Char: A string but with only one character
char = 'A'
print("This is a char:", char)
