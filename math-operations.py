# Addition: Adds numbers together
addition = 5 + 5
print("Addition Result:", addition)

# Subtraction: Subtracts the 2nd number from the 1st number
subtraction = 5 - 5
print("Subtraction Result:", subtraction)

# Multiplication: Multiplies numbers together
multiplication = 5 * 5
print("Multiplication Result:", multiplication)

# Division Float: Divides the 1st number by the 2nd number (result = float)
division_float = 5 / 4
print("Division Float Result:", division_float)

# Division Integer: Divides the 1st number by the 2nd number (result = integer)
division_integer = 5 // 4
print("Integer Division Result:", division_integer)

# Modulus: Returns the remainder of the division 
modulus = 5 % 4
print("Modulus Result:", modulus)

# Square Root: Calculates the square root of a number 
import math
square_root = math.sqrt(25)
print("Square Root Result:", square_root)

# Exponent: Raises the 1st number to the power of the exponent
exponent = 2**5
print("Exponent Result:", exponent)